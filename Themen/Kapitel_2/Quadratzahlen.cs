using System;

namespace Lernprojekt.Themen.Kapitel_2
{
    public class Quadratzahlen
    {
        public static void Methode()
        {
            // Berechnung und Ausgabe der Quadratzahlen von 1 bis 10
            for (int i = 1; i <= 10; i++)
            {
                Console.WriteLine("\n" + i * i);
            }
        }
    }
}
